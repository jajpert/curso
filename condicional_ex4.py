"""
4 - Faça um Programa que verifique se uma letra
digitada é vogal ou consoante.
"""

letra = input("Digite uma letra para saber se ela é uma vogal ou uma consoante: ")

letra = letra.lower()

if letra == "a":
    print("Esta letra é uma vogal")

elif letra == "e":
    print("Esta letra é uma vogal")

elif letra == "i":
    print("Esta letra é uma vogal")

elif letra == "o":
    print("Esta letra é uma vogal")

elif letra == "u":
    print("Esta letra é uma vogal")

else: 
    print("Esta letra é uma consoante")
